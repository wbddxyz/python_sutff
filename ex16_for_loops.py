def main():

    print('Example 1: Output the range 1 to 10: ')
    for n in range(1,11):
        print(n)

    print('Example 2: Output the range -10 to 10:')
    for n in range(-10, 11):
        print(n)

    print('Example 3: Output the range 0 to 100 inc 10')
    for n in range(-10, 11):
        print (n)



    print('Example 4: Output 99 - 1')
    for n in range(99,1):
        print(n)


if __name__=='__main__':
    main()
